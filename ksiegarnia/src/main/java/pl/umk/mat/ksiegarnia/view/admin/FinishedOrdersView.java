	/**
	 * shows filled table of all finished orders
	 * @param ArrayList<BookInformation> 
	 */
    public void showTable(ArrayList<BookInformation> allBooks) {
        //ArrayList<BookInformation> allBooks = User.loadFinishedOrders();
        DefaultTableModel model = (DefaultTableModel) tableBasket.getModel();
        for (BookInformation curBook : allBooks) {
            model.addRow(new Object[]{curBook.getTitle(), curBook.getAuthor(), curBook.getPH(), curBook.getDate(), curBook.getPrice() });
        }

    }
