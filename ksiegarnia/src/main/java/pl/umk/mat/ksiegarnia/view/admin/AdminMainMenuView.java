    /**
     * shows full box when true and empty box when false
     * @param Boolean
     */
    public void setNewOrderIcon(Boolean viewBox)
    {
    	if(viewBox)
    	{
    		boxEmpty.setVisible(false);    		
    		ThreadMessage.setText("Nowe zamowienie");
    		boxFull.setVisible(true);
    	}
    	else
    	{
    		boxFull.setVisible(false);
            ThreadMessage.setText("Brak zamowien");
            boxEmpty.setVisible(true);
    	}
    	
    }
    
